// Addition Operator: it is denoted by + 
let firstNumber = 10;
let secondNumber = 20;

console.log(firstNumber + secondNumber)

// Subtraction Operator: it is denoted by -
console.log(firstNumber - secondNumber)

// Multiplication Operator: it is denoted by *
console.log(firstNumber * secondNumber)

// Division Operator: it is denoted by /
console.log(firstNumber / secondNumber)

// Modulus Operator: it is denoted by % . It is used to bring out the remainder of a division
console.log(5 % 2 )

// Increment Operator: it is denoted by ++. It is used to increase the value of a variable by 1
let newNumber = 5;
newNumber++
console.log(newNumber)

// Decreament Operator: Denoted by --. Used to reduce a variable by 1
let DecreamentNumber = 5;
DecreamentNumber--
console.log(DecreamentNumber)

// Exponential Operator: Denoted by **
console.log(2**3)

